package fr.plum.plumlib.chat.sender;
/**
 * Chat option to choose if the prefix must appear
 * @author Meltwin
 * @since 1.1.0
 */
public enum PrefixOption {
    /**
     * A prefix will appear before the message
     */
    ADD_PREFIX,
    /**
     * No prefix will appear before the message
     */
    NO_PREFIX;
}