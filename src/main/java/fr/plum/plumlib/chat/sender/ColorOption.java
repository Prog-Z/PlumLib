package fr.plum.plumlib.chat.sender;
/**
 * Chat option to choose if the colorization appears
 * @author Meltwin
 * @since 1.1.0
 */
public enum ColorOption {
    /**
     * Colors must be shown in the rendered text
     */
    WITH_COLOR,
    /**
     * No colorization must be rendered
     */
    NO_COLOR;
}